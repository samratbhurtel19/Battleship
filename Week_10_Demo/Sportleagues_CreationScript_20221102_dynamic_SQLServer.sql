CREATE TABLE players (
    playerid INT PRIMARY KEY, 
	regnumber NVARCHAR(15), 
	lastname NVARCHAR(25), 
	firstname NVARCHAR(25),
	isactive INT DEFAULT 1,
	dob date
   );
ALTER TABLE players
    ALTER COLUMN lastname NVARCHAR(25) COLLATE Latin1_General_100_CS_AS;
ALTER TABLE players
	ALTER COLUMN firstname NVARCHAR(25) COLLATE Latin1_General_100_CS_AS;
	
CREATE TABLE teams (
    teamid INT PRIMARY KEY, 
	teamname VARCHAR(10),
	isactive INT, 
	jerseycolour VARCHAR(10)
   );
ALTER TABLE teams
	ALTER COLUMN teamname VARCHAR(10) COLLATE Latin1_General_100_CS_AS;

CREATE TABLE divs (
	divid INT PRIMARY KEY, 
	divname NVARCHAR(3), 
	isActive INT, 
	isDefault INT, 
	displayOrder INT DEFAULT 10
	);
   
CREATE TABLE rosters (
	rosterid INT IDENTITY(1,1) PRIMARY KEY, 
	playerid INT, 
	teamid INT, 
	Isactive INT, 
	jerseynumber INT DEFAULT 0,
	CONSTRAINT fk_rosters_player FOREIGN KEY (playerid) REFERENCES players(playerid),
	CONSTRAINT fk_rosters_team FOREIGN KEY (teamid) REFERENCES teams(teamid),
   );
   
-- CREATE teamsInDivs table
CREATE TABLE teamsInDivs
(
    linkid INT IDENTITY(1,1) PRIMARY KEY,
    teamid INT NOT NULL,
    divid INT NOT NULL,
	CONSTRAINT fk_teamdivs_teams FOREIGN KEY (teamID) REFERENCES teams(teamid),
	CONSTRAINT fk_teamdivs_divs FOREIGN KEY (divID) REFERENCES divs(divid)
);

CREATE TABLE games (
    gameid INT IDENTITY(1,1) PRIMARY KEY, 
	divid INT, 
	Gamenum INT, 
	gamedatetime DATE, 
	hometeam INT, 
	homescore INT, 
	visitteam INT, 
	visitscore INT, 
	locationid INT, 
	Isplayed INT, 
	notes NVARCHAR(50),
	CONSTRAINT fk_games_hometeam FOREIGN KEY (hometeam) REFERENCES teams(teamid),
	CONSTRAINT fk_games_visitteam FOREIGN KEY (visitteam) REFERENCES teams(teamid),
	CONSTRAINT fk_games_locations FOREIGN KEY (locationid) REFERENCES locations(locationid)
);
   
-- CREATE goalScorers table
-- Prompt ******  Inserting Goal Scorers Records table ....

CREATE TABLE goalScorers
(
    goalid INT IDENTITY(1,1) PRIMARY KEY,
    gameid INT NOT NULL,
    playerid INT NOT NULL,
    teamid INT NOT NULL,
    numgoals INT DEFAULT 0,
    numassists INT DEFAULT 0,
	CONSTRAINT fk_scorers_games FOREIGN KEY (gameid) REFERENCES games(gameid),
	CONSTRAINT fk_scorers_players FOREIGN KEY (playerid) REFERENCES players(playerid),
	CONSTRAINT fk_scorers_teams FOREIGN KEY (teamid) REFERENCES teams(teamid)
    );

